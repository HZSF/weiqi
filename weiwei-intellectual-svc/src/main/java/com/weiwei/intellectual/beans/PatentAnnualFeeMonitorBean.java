package com.weiwei.intellectual.beans;

public class PatentAnnualFeeMonitorBean {
	public String patent_id;
	public String title;
	public String applicant;
	public String apply_date;
	public String expire_date;
	public String expire_status;
	public String payment_status;
	public String annual_fee;
	public String getPatent_id() {
		return patent_id;
	}
	public void setPatent_id(String patent_id) {
		this.patent_id = patent_id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getApplicant() {
		return applicant;
	}
	public void setApplicant(String applicant) {
		this.applicant = applicant;
	}
	public String getApply_date() {
		return apply_date;
	}
	public void setApply_date(String apply_date) {
		this.apply_date = apply_date;
	}
	public String getExpire_date() {
		return expire_date;
	}
	public void setExpire_date(String expire_date) {
		this.expire_date = expire_date;
	}
	public String getExpire_status() {
		return expire_status;
	}
	public void setExpire_status(String expire_status) {
		this.expire_status = expire_status;
	}
	public String getPayment_status() {
		return payment_status;
	}
	public void setPayment_status(String payment_status) {
		this.payment_status = payment_status;
	}
	public String getAnnual_fee() {
		return annual_fee;
	}
	public void setAnnual_fee(String annual_fee) {
		this.annual_fee = annual_fee;
	}
	
}
